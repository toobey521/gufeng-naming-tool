_CHAR_INFO = {}
_CHAR_SET = set()
_WX_INDEX = {}
_GEN_INDEX = {}

for t in CHAR_TUPLE:
    c = t[0]
    info = {
        'char': c, 'pinyin': t[1], 'tone': t[2],
        'strokes': t[3], 'wuxing': t[4], 'gender': t[5],
        'tags': t[6], 'meaning': t[7],
    }
    _CHAR_INFO[c] = info
    _CHAR_SET.add(c)
    _WX_INDEX.setdefault(t[4], set()).add(c)
    _GEN_INDEX.setdefault(t[5], set()).add(c)

CHAR_SET = _CHAR_SET
CHAR_DICT = _CHAR_INFO

def get_char_info(char):
    """获取单个汉字完整信息"""
    return _CHAR_INFO.get(char)

def get_chars_by_wuxing(wuxing):
    """按五行获取字符集"""
    return _WX_INDEX.get(wuxing, set())

def get_chars_by_gender(gender):
    """按性别获取字符集 (m/f/n)"""
    return _GEN_INDEX.get(gender, set())

def get_wuxing_gender(char):
    """获取字符的五行和性别信息"""
    info = _CHAR_INFO.get(char)
    if not info:
        return []
    return [(info['wuxing'], info['gender'])]

def search_chars(wuxing=None, gender=None, keyword=None, max_strokes=None, min_strokes=None):
    """多条件查询汉字
    wuxing: 五行过滤
    gender: 性别过滤 (m/f/n)
    keyword: 拼音/含义关键词搜索
    max_strokes/min_strokes: 笔画范围过滤
    """
    results = []
    for c, info in _CHAR_INFO.items():
        if wuxing and info['wuxing'] != wuxing: continue
        if gender and info['gender'] != gender: continue
        if keyword and keyword not in info['pinyin'] and keyword not in info['tags'] and keyword not in info['meaning'] and keyword not in c: continue
        if max_strokes and info['strokes'] > max_strokes: continue
        if min_strokes and info['strokes'] < min_strokes: continue
        results.append(info)
    return sorted(results, key=lambda x: x['strokes'])

